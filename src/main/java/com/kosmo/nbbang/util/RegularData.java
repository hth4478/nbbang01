package com.kosmo.nbbang.util;

public class RegularData {
	
	private String name;
	private String cost;
	public RegularData(String name, String cost) {
		super();
		this.name = name;
		this.cost = cost;
	}
	public String getName() {
		return name;
	}
	public String getCost() {
		return cost;
	}


}
